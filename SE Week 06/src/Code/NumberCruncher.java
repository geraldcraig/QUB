package Code;

/**
 * Class used to manipulate numbers
 * 
 * @author amcgowan
 */
public class NumberCruncher {

	/**
	 * Method adds two numbers and returns the result
	 * 
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int addNumbers(int num1, int num2) {
		return num1 + num2;
	}

	/**
	 * Method add three numbers and returns the result
	 * 
	 * @param num1
	 * @param num2
	 * @param num3
	 * @return
	 */
	public int addNumbers(int num1, int num2, int num3) {
		return num1 + num2 + num3;
	}

	
	public int multiNumbers(int num1, int num2) {
		return num1 * num2;
	}

}

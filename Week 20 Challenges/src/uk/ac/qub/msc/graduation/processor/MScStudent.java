package uk.ac.qub.msc.graduation.processor;

/**
 * @author geraldcraig
 *
 */
public class MScStudent extends Student {
	
	private int program;
	private int cFound;
	private int dBs;
	private int web;
	private int sEngin;
	/**
	 * @return the program
	 */
	public int getProgram() {
		return program;
	}
	/**
	 * @param program the program to set
	 */
	public void setProgram(int program) {
		this.program = program;
	}
	/**
	 * @return the cFound
	 */
	public int getcFound() {
		return cFound;
	}
	/**
	 * @param cFound the cFound to set
	 */
	public void setcFound(int cFound) {
		this.cFound = cFound;
	}
	/**
	 * @return the dBs
	 */
	public int getdBs() {
		return dBs;
	}
	/**
	 * @param dBs the dBs to set
	 */
	public void setdBs(int dBs) {
		this.dBs = dBs;
	}
	/**
	 * @return the web
	 */
	public int getWeb() {
		return web;
	}
	/**
	 * @param web the web to set
	 */
	public void setWeb(int web) {
		this.web = web;
	}
	/**
	 * @return the sEngin
	 */
	public int getsEngin() {
		return sEngin;
	}
	/**
	 * @param sEngin the sEngin to set
	 */
	public void setsEngin(int sEngin) {
		this.sEngin = sEngin;
	}
	public MScStudent() {
		super();
		
	}
	
	

}
